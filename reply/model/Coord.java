package reply.model;
public class Coord {
	int x;
	int y;
	public Coord(int x, int y) {
		this.x = x;
		this.y = y;
	}
	public Coord() {}
	public int getX() {
		return this.x;
	}
	public int getY() {
		return this.y;
	}
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}
}
